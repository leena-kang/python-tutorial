# gives more access to math functions ("math" = modedule)
from math import *

# common functions used relating to numbers

# abs() --> takes abs num
my_num = -5
print(abs(my_num))

# pow(num, num) --> takes 1st num to the power of 2nd num
print(pow(3,2))

# max() --> returns largest num // min() --> returns smallest num
print(max(4,6))

# round() --> rounds num
print(round(3.2)) 

# floor() --> largest integer (will output 3 for this ex)
print(floor(3.7))

# ceil() --> rounds up
print(ceil(3.7))

# sqrt() --> square root
print(sqrt(36))
